//add() & addAll() using add(index, object)
import java.util.*;
class AddMethodIndex
{
	public static void main(String[] args) 
	{
		ArrayList a = new ArrayList();
			a.add(10);
			a.add("kartik");
			System.out.println(a);
			a.add(1,20);
			a.add(3,30);
			System.out.println(a);

		

		ArrayList a1 = new ArrayList();
			a1.add(50000);
			a1.add("hello");
			System.out.println(a1);
			
			a1.addAll(0,a);
			System.out.println(a1);

			a1.add("Good Morning");
			a1.addAll(a);
			System.out.println(a1);
	}
}
